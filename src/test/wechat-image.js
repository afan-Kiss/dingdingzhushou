#!/usr/bin/env node
const fs = require('fs');
const path = require('path');
const { loadConfig } = require('../config');
const { PROJECT_ROOT } = require('../logger');
const { WxbotAdapter } = require('../wechat/wxbotAdapter');
const { logger } = require('../logger');

async function main() {
  const config = loadConfig();
  const wx = new WxbotAdapter(config);
  const testPath = path.join(PROJECT_ROOT, 'screenshots', '_test_image.png');

  const pngHeader = Buffer.from([
    0x89, 0x50, 0x4e, 0x47, 0x0d, 0x0a, 0x1a, 0x0a, 0x00, 0x00, 0x00, 0x0d, 0x49, 0x48, 0x44, 0x52,
    0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01, 0x08, 0x02, 0x00, 0x00, 0x00, 0x90, 0x77, 0x53,
    0xde, 0x00, 0x00, 0x00, 0x0c, 0x49, 0x44, 0x41, 0x54, 0x08, 0xd7, 0x63, 0xf8, 0xcf, 0xc0, 0x00,
    0x00, 0x03, 0x01, 0x01, 0x00, 0x18, 0xdd, 0x8d, 0xb4, 0x00, 0x00, 0x00, 0x00, 0x49, 0x45, 0x4e,
    0x44, 0xae, 0x42, 0x60, 0x82,
  ]);
  fs.mkdirSync(path.dirname(testPath), { recursive: true });
  fs.writeFileSync(testPath, pngHeader);

  const result = await wx.sendImage(testPath);
  console.log('发送结果:', result);
  process.exit(0);
}

main().catch((err) => {
  logger.error('test:wechat-image 失败', { error: err.message });
  process.exit(1);
});
